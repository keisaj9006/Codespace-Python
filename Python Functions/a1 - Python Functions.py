# Function to show the content of a list
def showListContent(list):
    print("The content of the list is:", list)

# Function to find the maximum value in a list
def showListMaxVal(list):
    maxValue = max(list)
    print("The max value in the list:", maxValue)

# Sample list
sample_list = [10, 2, 3, 4, 7]

# Show the content of the list
showListContent(sample_list)

# Find the maximum value in the list
showListMaxVal(sample_list)
